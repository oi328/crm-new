i18n
  .use(initReactI18next)
  .init({
    resources,
    // Initialize language from system preferences if available
    lng: (() => {
      try {
        if (typeof window !== 'undefined' && window.localStorage) {
          const prefsRaw = window.localStorage.getItem('systemPrefs')
          if (prefsRaw) {
            const prefs = JSON.parse(prefsRaw)
            if (prefs && prefs.language) return prefs.language
          }
          const directLang = window.localStorage.getItem('language')
          if (directLang) return directLang
        }
      } catch (err) {}
      return 'en'
    })(),
    fallbackLng: 'en',
    interpolation: {
      escapeValue: false
    }
  });

