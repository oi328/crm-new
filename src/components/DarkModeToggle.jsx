import React, { useState, useEffect } from 'react';

// أيقونات بسيطة باستخدام SVG
const SunIcon = () => (
    <svg xmlns="http://www.w3.org/2000/svg" width="24" height="24" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round">
        <circle cx="12" cy="12" r="5"></circle><line x1="12" y1="1" x2="12" y2="3"></line><line x1="12" y1="21" x2="12" y2="23"></line><line x1="4.22" y1="4.22" x2="5.64" y2="5.64"></line><line x1="18.36" y1="18.36" x2="19.78" y2="19.78"></line><line x1="1" y1="12" x2="3" y2="12"></line><line x1="21" y1="12" x2="23" y2="12"></line><line x1="4.22" y1="19.78" x2="5.64" y2="18.36"></line><line x1="18.36" y1="5.64" x2="19.78" y2="4.22"></line>
    </svg>
);

const MoonIcon = () => (
    <svg xmlns="http://www.w3.org/2000/svg" width="24" height="24" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round">
        <path d="M21 12.79A9 9 0 1 1 11.21 3 7 7 0 0 0 21 12.79z"></path>
    </svg>
);

function DarkModeToggle() {
  // 1. نبدأ بقراءة الوضع المحفوظ في المتصفح أو نفترض الوضع الفاتح
  const [isDarkMode, setIsDarkMode] = useState(() => localStorage.getItem('theme') === 'dark');

  // 2. نستخدم هذا التأثير لتطبيق الكلاس على <html> وحفظ اختيار المستخدم
  useEffect(() => {
    const root = window.document.documentElement; // هذا هو العنصر <html>
    if (isDarkMode) {
      root.classList.add('dark');
      localStorage.setItem('theme', 'dark'); // حفظ الاختيار
    } else {
      root.classList.remove('dark');
      localStorage.setItem('theme', 'light'); // حفظ الاختيار
    }
  }, [isDarkMode]);

  return (
    <button
      onClick={() => setIsDarkMode(!isDarkMode)}
      // كلاسات لتثبيت الزر في زاوية الشاشة لسهولة الوصول
      className="fixed top-4 right-4 z-50 p-2 text-gray-800 dark:text-gray-200 bg-gray-100 dark:bg-gray-800 rounded-full hover:bg-gray-200 dark:hover:bg-gray-700 focus:outline-none shadow-md"
    >
      {isDarkMode ? <SunIcon /> : <MoonIcon />}
    </button>
  );
}

export default DarkModeToggle;
